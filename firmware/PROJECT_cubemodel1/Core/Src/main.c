void SPI_Transmit(uint8_t *data, uint16_t size)
{
//	uint16_t temp;
    for(uint16_t i = 0; i < size; i++)
    {
        // Wait until TX buffer is empty
    	while(!LL_SPI_IsActiveFlag_TXE(SPI2));

        // Transmit byte
        LL_SPI_TransmitData8(SPI2, data[i]);
    }

    // Wait until the transmission is complete
    while(!LL_SPI_IsActiveFlag_TXE(SPI2));
    while(LL_SPI_IsActiveFlag_BSY(SPI2));
}

void SPI_Receive(uint8_t *data, uint16_t size)
{
    for(uint16_t i = 0; i < size; i++)
    {
    	while(LL_SPI_IsActiveFlag_BSY(SPI2));

        // Receive byte
    	while(!LL_SPI_IsActiveFlag_RXNE(SPI2));
        data[i] = LL_SPI_ReceiveData8(SPI2);
        LL_SPI_ClearFlag_OVR(SPI2);
    }
}

void SPI_TransmitReceive(uint8_t *TxData, uint8_t *RxData, uint16_t size)
{
    for(uint16_t i = 0; i < size; i++)
    {
    	SPI_Transmit(&TxData[i], 1);
    	SPI_Receive(&RxData[i], 1);
    }
}

