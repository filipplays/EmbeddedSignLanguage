uint16_t average_rgb565(uint16_t color1, uint16_t color2, uint16_t color3, uint16_t color4) {
    // Extract red, green, and blue components
    uint16_t r1 = (color1 >> 11) & 0x1F;
    uint16_t g1 = (color1 >> 5) & 0x3F;
    uint16_t b1 = color1 & 0x1F;

    uint16_t r2 = (color2 >> 11) & 0x1F;
    uint16_t g2 = (color2 >> 5) & 0x3F;
    uint16_t b2 = color2 & 0x1F;

    uint16_t r3 = (color3 >> 11) & 0x1F;
    uint16_t g3 = (color3 >> 5) & 0x3F;
    uint16_t b3 = color3 & 0x1F;

    uint16_t r4 = (color4 >> 11) & 0x1F;
    uint16_t g4 = (color4 >> 5) & 0x3F;
    uint16_t b4 = color4 & 0x1F;

    // Calculate averages
    uint16_t r_avg = (r1 + r2 + r3 + r4) / 4;
    uint16_t g_avg = (g1 + g2 + g3 + g4) / 4;
    uint16_t b_avg = (b1 + b2 + b3 + b4) / 4;

    // Combine averages into a single color
    uint16_t color_avg = (r_avg << 11) | (g_avg << 5) | b_avg;

    return color_avg;
}

