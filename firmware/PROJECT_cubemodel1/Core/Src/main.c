uint32_t read_fifo_length(void) {
  uint32_t len1, len2, len3, length = 0;
  len1 = camReadReg(0x42);
  len2 = camReadReg(0x43);
  len3 = camReadReg(0x44) & 0x7f;
  length = ((len3 << 16) | (len2 << 8) | len1) & 0x07fffff;
  return length;
}
